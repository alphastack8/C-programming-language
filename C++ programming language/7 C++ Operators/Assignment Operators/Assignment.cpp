#include <iostream>
using namespace std;

int main() {
  int x = 10;

  x += 5;  // same as x = x + 5
  cout << x << "\n"; // 15

  x *= 2;  // same as x = x * 2
  cout << x << "\n"; // 30

  return 0;
}
