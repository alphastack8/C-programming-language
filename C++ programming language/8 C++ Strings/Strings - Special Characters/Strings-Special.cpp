#include <iostream>
using namespace std;
// The sequence \"  inserts a double quote in a string:

int main() {
  string txt = "We are the so-called \"Vikings\" from the north.";
  cout << txt;
  return 0;
}
// output We are the so-called "Vikings" from the north.


